public class do_while {
    public static void main(String args[]) {

        int i = 1;
        do {
            System.out.println(i);
            i++;
        } while (i <= 10);

        System.out.println("\nReverse : ");

        int j = 10;
        do {
            System.out.println(j);
            j--;
        } while (j >= 1);

    }
}