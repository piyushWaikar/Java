public class normal_operators {
    public static void main(String args[]) {

        int a = 10;
        int b = 30;
        int sum = a + b;
        int diff = a - b;
        int div = a / b;
        int mul = a * b;
        int mod = a % b;
        System.out.println(sum);
        System.out.println(diff);
        System.out.println(div);
        System.out.println(mul);
        System.out.println(mod);

    }
}