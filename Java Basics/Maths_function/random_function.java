// Random function gives radom values . 

public class random_function {
    public static void main(String args[]) {

        System.out.println(Math.random()); // This function return double value . 

        System.out.println((int) Math.random()); // This function return zero .
        
        System.out.println((int) (Math.random() * 100)); // To get value into 100 we , need to multiply by 100 .

    }
}