public class casting {
public static void main(String args[]){
        
    double price = 100;
    double finalprice = price+18;
    System.out.println(finalprice);
    

    // Casting : casting is the tecnique of making change in variable types to get required type of output .

    int priceon = 100;
    int finalpri = priceon + (int)18.0;     // here we are casting double into int type .
    System.out.println(finalpri);
    

    }
}